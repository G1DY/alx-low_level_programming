#include "3-calc.h"
/**
 * op_add - adds a and b
 * @a: first integer
 * @b: second interger
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts b from a
 * @a: first integer
 * @b: second integer
 *
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - calculates the multiplication of a and b
 * @a: first integer
 * @b: second integer
 *
 * Return: the multiplication of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - calculates division
 * @a: first integer
 * @b: second integer
 *
 * Return: the division of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculates the mode of a and b
 * @a: first integer
 * @b: second integer
 *
 * Return: the modulus of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
